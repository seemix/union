const booksArray = books.split('\n');
const sequenceNumberOfCurrentDay = (date: Date): number => {
    const startOfYear = new Date(date.getFullYear(), 0, 0);
    const diff = date.getTime() - startOfYear.getTime();
    const oneDay = 1000 * 60 * 60 * 24;
    return Math.floor(diff / oneDay);
};
const dayNumber = sequenceNumberOfCurrentDay(new Date());

export const biblePassage = () => {
    if (dayNumber === 60) return booksArray[59];
    return booksArray[dayNumber];
};